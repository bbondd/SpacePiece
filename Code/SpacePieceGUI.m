function pushbutton1_Callback(hObject, eventdata, handles)
x1 = str2double(get(handles.x1, 'String'));
y1 = str2double(get(handles.y1, 'String'));
z1 = str2double(get(handles.z1, 'String'));
x2 = str2double(get(handles.x2, 'String'));
y2 = str2double(get(handles.y2, 'String'));
z2 = str2double(get(handles.z2, 'String'));
x3 = str2double(get(handles.x3, 'String'));
y3 = str2double(get(handles.y3, 'String'));
z3 = str2double(get(handles.z3, 'String'));
angle1 = pi * str2double(get(handles.angle1, 'String'));
angle2 = pi * str2double(get(handles.angle2, 'String'));
angle3 = pi * str2double(get(handles.angle3, 'String'));
imagePath1 = get(handles.imagePath1, 'String');
imagePath2 = get(handles.imagePath2, 'String');
imagePath3 = get(handles.imagePath3, 'String');

vectors = [x1 x2 x3; y1 y2 y3; z1 z2 z3];
angles = [angle1 angle2 angle3];
imagePaths = char(imagePath1, imagePath2, imagePath3); 
main(vectors, angles, imagePaths);




